#2.Faça um Programa que peça um valor e mostre na tela se o valor é positivo ou negativo.

#Armazena a variavel
number1 = int(input("digite o valor: "))

#SE o valor for maior que zero imprimi positivo , se nao imprimi negativo
if(number1 > 0):
    print("positivo")
else:
    print("negativo")